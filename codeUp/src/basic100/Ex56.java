package basic100;
import java.util.Scanner;

public class Ex56 {

	public static void main(String[] args) {
		// 참/거짓이 서로 같을 때에만 참 출력하기
		Scanner sc = new Scanner(System.in);
		
		int a = sc.nextInt();
		int b = sc.nextInt();
		
		if (a == b) {
			System.out.println("1");
		} else {
			System.out.println("0");
		}
		
		sc.close();
	}

}
